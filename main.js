function add(a, b) {
    let sum = a + b; 
    return sum;
}

function subtract(a, b) {
    let difference = a - b; 
    return difference; 
}

function multiply(a, b) {
    let product = a * b; 
    return product;
}

function divide(a, b) {
    let quotient = a / b; 
    return quotient;
}

console.log(add(4, 9)); 
console.log(subtract(4, 9)); 
console.log(multiply(4, 9)); 
console.log(divide(36, 9)); 